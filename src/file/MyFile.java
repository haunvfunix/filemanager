package file;

public class MyFile {
	String name;
	int Size;
	String FullPath;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSize() {
		return Size;
	}
	public void setSize(int size) {
		Size = size;
	}
	public String getFullPath() {
		return FullPath;
	}
	public void setFullPath(String fullPath) {
		FullPath = fullPath;
	}
	
}